package javaClassWithMrDuran.day0_10.day07;

public class forLoop2 {
    public static void main(String[] args) {
        for (int i = 100 ; i >= 0 ; i -= 2 ){
            System.out.println(i);
        }
        for (int i = 2000 ; i >= 1000 ; i -= 2 ){
            System.out.println(i);
        }
    }
}
