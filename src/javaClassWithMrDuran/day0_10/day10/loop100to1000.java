package javaClassWithMrDuran.day0_10.day10;

public class loop100to1000 {
    public static void main(String[] args) {
        for (int i =1 ; i<10 ; i++){
            for (int j =0 ; j<10 ; j++){
                for (int k =0 ; k<10 ; k++){
                    System.out.println(""+i+j+k);
                }
            }
        }
    }
}
