package javaClassWithMrDuran.day0_10.day09;

public class CalendarMix {
    public static void main(String[] args) {
        //while and for
        int month = 1;
        while (month<13){
            for (int day = 1 ; day<31 ; day++){
                System.out.println(month+"/"+day);
            }
            month++;
        }
        //for while



    }
}
