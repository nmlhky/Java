public class multipation {
    public static void main(String[] args) {
        for (int i = 1 ; i < 11 ; i++){
            for (int j = 1 ; j < 11 ; j++) {
                System.out.format("%d X %d = %d \n", i, j, i * j);
            }
            System.out.println("-----------");
        }

    }
}
